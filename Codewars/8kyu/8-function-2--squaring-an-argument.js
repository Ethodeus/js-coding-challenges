//* NAME -> Function 2 - squaring an argument

//TODO INSTRUCTIONS -> Now you have to write a function that takes an argument and returns the square of it.

//SOLUTION(S) ->

const square = (a) => Math.pow(a, 2);

//TEST CASES ->

console.log(square(5)); // -> 25
console.log(square(10)); // -> 100
console.log(square(8)); // -> 64
